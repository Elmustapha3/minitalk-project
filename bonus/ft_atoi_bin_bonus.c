#include "minitalk_bonus.h"

int	ft_atoi_bin(char str)
{
	int		i;
	long	r;

	i = 0;
	r = 0;
	if (str >= '0' && str <= '1')
	{
		r = r * 2 + (str - 48);
		i++;
	}
	return (r);
}
